# ─────────────────────────────────────────────────────────────────────────────
# Per-(device, dtype) cache (review M5)
# ─────────────────────────────────────────────────────────────────────────────
#
# The codebook tensors materialise from the Python tuples; allocating them
# per STE call would burn ~16 KB / call for the magnitude grid. The cache
# is keyed once per (device, dtype); a single-GPU run sees one tensor each.

_IQ2XS_GRID_CACHE: dict[tuple[torch.device, torch.dtype], torch.Tensor] = {}
_KSIGNS_IQ2XS_CACHE: dict[tuple[torch.device, torch.dtype], torch.Tensor] = {}


def get_iq2xs_grid(device: torch.device, dtype: torch.dtype) -> torch.Tensor:
    """Return the IQ2_XS magnitude codebook as a ``(512, 8)`` tensor.

    Each row is the 8-magnitude pattern from one ``iq2xs_grid[]`` entry,
    unpacked little-endian (low byte = element 0).
    """
    key = (device, dtype)
    cached = _IQ2XS_GRID_CACHE.get(key)
    if cached is not None:
        return cached
    # Unpack each uint64 into 8 little-endian bytes.
    flat: list[int] = []
    for val in IQ2XS_GRID_RAW:
        for i in range(8):
            flat.append((val >> (i * 8)) & 0xff)
    tensor = torch.tensor(flat, dtype=dtype, device=device).view(512, 8)
    _IQ2XS_GRID_CACHE[key] = tensor
    return tensor


def get_ksigns_iq2xs(device: torch.device, dtype: torch.dtype) -> torch.Tensor:
    """Return the IQ2_XS sign codebook expanded to ``(128, 8)`` of +-1 floats.

    Each input row is the bit-pattern from ``KSIGNS_IQ2XS[i]``; each output
    row contains 8 signs in {-1, +1} (bit clear => +1, bit set => -1, per
    ggml's convention) with even parity over the 8 signs.
    """
    key = (device, dtype)
    cached = _KSIGNS_IQ2XS_CACHE.get(key)
    if cached is not None:
        return cached
    expanded: list[list[float]] = []
    for bits in KSIGNS_IQ2XS:
        row = [-1.0 if (bits >> i) & 1 else 1.0 for i in range(8)]
        expanded.append(row)
    tensor = torch.tensor(expanded, dtype=dtype, device=device)
    _KSIGNS_IQ2XS_CACHE[key] = tensor
    return tensor
